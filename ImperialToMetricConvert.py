def InchToMilimeter (dimension):
    #convert inch to milimeter
    return dimension*25.4

def InchToCentimeter (dimension):
    #convert inch to centimeter
    return dimension*2.54

def InchToMeter (dimension):
    #convert inch to meter
    return dimension*0.0254

def InchToKilometer (dimension):
    #convert inch to kilometer
    return dimension*0.0000254


